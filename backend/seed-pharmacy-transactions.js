import { db } from './src/config/db.js'

const ORG_ID = 'org-demo'

function getRandomItem(arr) {
  return arr[Math.floor(Math.random() * arr.length)]
}

function rInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min
}

async function seedPharmacyTransactions() {
  console.log('💊 SEEDING PHARMACY TRANSACTIONS (Prescriptions, Sales, POs)...\n')

  const patients = await db.patient.findMany({ where: { organizationId: ORG_ID }, take: 100 })
  const doctors = await db.user.findMany({ where: { organizationId: ORG_ID, role: 'doctor' }, take: 20 })
  const drugs = await db.pharmacyDrug.findMany({ where: { organizationId: ORG_ID }, take: 50 })
  
  if (!patients.length || !doctors.length || !drugs.length) {
    console.error('❌ Missing required master data (patients, doctors, or drugs)')
    process.exit(1)
  }

  // 1. Create Prescriptions
  console.log('📝 Creating 50 Prescriptions...')
  let prescriptions = []
  for (let i = 0; i < 50; i++) {
    const patient = getRandomItem(patients)
    const doctor = getRandomItem(doctors)
    const pDrugs = [getRandomItem(drugs), getRandomItem(drugs)] // 2 items
    
    const items = pDrugs.map(d => ({
      drugId: d.id,
      drugName: d.drugName,
      dosage: '1 Tablet',
      frequency: 'BD',
      duration: '5 Days',
      quantity: 10,
      instructions: 'After meals'
    }))

    const p = await db.prescription.create({
      data: {
        organizationId: ORG_ID,
        patientId: patient.id,
        doctorId: doctor.id,
        prescriptionDate: new Date(),
        items: JSON.stringify(items),
        status: 'pending',
        notes: 'Generated by demo script'
      }
    })
    prescriptions.push(p)
  }
  console.log('✅ Created 50 Prescriptions')

  // 2. Create Pharmacy Sales
  console.log('\n💰 Creating 50 Pharmacy Sales...')
  for (let i = 0; i < 50; i++) {
    const patient = getRandomItem(patients)
    const isRx = Math.random() > 0.5
    const rx = isRx ? getRandomItem(prescriptions) : null
    
    const sDrugs = [getRandomItem(drugs)]
    let totalAmount = 0
    
    const items = sDrugs.map(d => {
      const qty = rInt(1, 5)
      const price = d.sellingPrice || 100
      const total = qty * price
      totalAmount += total
      
      return {
        drugId: d.id,
        batchId: null, // Simplified for demo
        drugName: d.drugName,
        quantity: qty,
        unitPrice: price,
        total: total
      }
    })

    await db.pharmacySale.create({
      data: {
        organizationId: ORG_ID,
        patientId: patient.id,
        prescriptionId: rx ? rx.id : null,
        saleType: rx ? 'prescription' : 'otc',
        items: JSON.stringify(items),
        subtotal: totalAmount,
        discountAmount: 0,
        taxAmount: 0,
        totalAmount: totalAmount,
        paymentStatus: 'paid',
        paymentMethod: 'cash',
        amountPaid: totalAmount,
        receiptNumber: `RCPT-2026-${String(i+1).padStart(4, '0')}-${Date.now().toString().slice(-4)}`
      }
    })
  }
  console.log('✅ Created 50 Pharmacy Sales')

  // 3. Create Purchase Orders
  console.log('\n📦 Creating 10 Purchase Orders...')
  for (let i = 0; i < 10; i++) {
    const poDrugs = [getRandomItem(drugs), getRandomItem(drugs)]
    let totalAmount = 0
    
    const items = poDrugs.map(d => {
      const qty = rInt(50, 200)
      const cost = d.costPrice || 50
      const total = qty * cost
      totalAmount += total
      
      return {
        drugId: d.id,
        drugName: d.drugName,
        strength: d.strength || '10mg',
        quantityOrdered: qty,
        quantityReceived: qty,
        unitCost: cost,
        totalCost: total,
        batchNumber: `BATCH-${rInt(1000, 9999)}`,
        expiryDate: new Date(Date.now() + 31536000000) // +1 year
      }
    })

    await db.pharmacyPurchaseOrder.create({
      data: {
        organizationId: ORG_ID,
        poNumber: `PO-2026-${String(i+1).padStart(4, '0')}`,
        status: 'received',
        supplierName: 'MediLife Pharma Distributors',
        orderDate: new Date(),
        receivedDate: new Date(),
        items: JSON.stringify(items),
        totalAmount: totalAmount,
        notes: 'Bulk stock purchase'
      }
    })
  }
  console.log('✅ Created 10 Purchase Orders')

  console.log('\n🎉 ALL PHARMACY TRANSACTIONS SEEDED SUCCESSFULLY!')
  process.exit(0)
}

seedPharmacyTransactions().catch(e => {
  console.error('Error:', e)
  process.exit(1)
})
